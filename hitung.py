import math

def tambah(bil1, bil2):
    hasil = 7 + 2
    print("Hasil Tambah Dari",7, "+", 2, "=", hasil)

def kurang(bil1, bil2):
    hasil = 9 - 4
    print("Hasil Pengurangan Dari",9, "-", 4, "=", hasil)

def kali(bil1, bil2):
    hasil = 5 * 5
    print("Hasil Perkalian Dari",3, "*", 4, "=", hasil)

def bagi(bil1, bil2):
    hasil = 16 / 4
    print("Hasil Pembagian Dari",13, "/", 5, "=", hasil)

def pangkat(bil1, bil2):
    hasil = math.pow(5, 3)
    print("Hasil Pemangkatan Dari",6, "^", 2, "=", hasil)